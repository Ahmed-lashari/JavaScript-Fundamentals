// Use of Arthimatic Operators exactly like we use in Mathematics

// ==========================
// 1. Addition (+)
// ==========================

let bread = 2
let jam = 3
let total = bread + jam

console.log("Total breads:", total)  // output: 5

// ==========================
// 2. Subtraction (-)
// ==========================


let totalStudents = 30
let absentStudents = 5
let presentStudents = totalStudents - absentStudents

console.log("Present students:", presentStudents)  // output: 25

// ==========================
// 3. Multiplication (*)
// ==========================

let pakorasPerPlate = 6
let plates = 4
let totalPakoras = pakorasPerPlate * plates

console.log("Total pakoras:", totalPakoras)  // output: 24

// ==========================
// 4. Division (/)
// ==========================

let totalChocolates = 20
let kids = 5
let perKid = totalChocolates / kids

console.log("Each kid gets:", perKid)  // output: 4

// ==========================
// 5. Modulus (%)
// ==========================

let oranges = 10
let people = 3
let remaining = oranges % people

console.log("Leftover oranges:", remaining)  // output: 1

// ==========================
// BONUS: Real life example
// ==========================

let biryaniPlates = 7
let guests = 2

let eachGets = biryaniPlates / guests
let leftAfterServe = biryaniPlates % guests

console.log("Har guest ko milay:", eachGets)         // output: 3.5
console.log("Poora nahi mila, leftover:", leftAfterServe)  // output: 1

// Each guest got 3.5 plates of Briyani and 1 plate was left

// ==========================
// Recap
// ==========================
// +  addition 
// -  subtraction
// *  multiplication
// /  division
// %  modulus | modulo (reminder left after division)

